package com.main.day5;

public class ValueChange {

    int x_1, x_2, y_1, y_2;

    public ValueChange(int x_1, int y_1, int x_2, int y_2) {
        this.x_1 = x_1;
        this.x_2 = x_2;
        this.y_1 = y_1;
        this.y_2 = y_2;
    }

    public int getX_1() {
        return x_1;
    }

    public int getX_2() {
        return x_2;
    }

    public int getY_1() {
        return y_1;
    }

    public int getY_2() {
        return y_2;
    }
}
